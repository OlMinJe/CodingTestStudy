// 단속카메라

/**
 * ✏️ 문제 개요
 * - 차량들이 고속도로를 이동하는 구간이 주어질 때,
 *   모든 차량이 최소 한 번은 단속 카메라에 찍히도록 카메라를 설치
 *
 * ✏️ 목표
 * - 최소 개수의 카메라로 모든 차량의 경로를 커버
 *
 * ✏️ 해결 전략 (탐욕법)
 * 1. 차량의 진출 지점을 기준으로 오름차순 정렬
 * 2. 진출 지점에 카메라를 설치
 * 3. 해당 지점 이전에 진입한 차량들은 이 카메라로 모두 커버
 * 4. 이 범위를 벗어나는 차량이 나타나면 새 카메라 설치
 */

// 기본 풀이
function solution(routes) {
  // 1️⃣ 차량을 진출 지점 기준으로 정렬
  routes.sort((a, b) => a[1] - b[1]);

  let cameraCount = 0; // 설치된 카메라 수
  let lastCameraPosition = -30001; // 가장 최근 설치된 카메라 위치 (최솟값으로 초기화)

  // 2️⃣ 모든 차량을 순회하며 카메라 설치
  for (let [entry, exit] of routes) {
    // 현재 차량이 이전 카메라 범위에 포함되지 않으면
    if (entry > lastCameraPosition) {
      // 새로운 카메라를 이 차량의 진출 지점에 설치
      lastCameraPosition = exit;
      cameraCount++; // 카메라 추가
    }
    // 포함되는 경우는 이미 찍혔으므로 패스
  }

  return cameraCount; // 최소 카메라 개수 반환
}
